# comments.py - Examples from W3Schools Python Comments

# Example 1: Single line comment
#This is a comment
print("Hello, World!")

# Example 2: Inline comment
print("Hello, World!") #This is a comment

# Example 3: Commented out code
#print("Hello, World!")
print("Cheers, Mate!")

# Example 4: Multiline comment using #
#This is a comment
#written in
#more than just one line
print("Hello, World!")

# Example 5: Multiline comment using triple quotes
"""
This is a comment
written in
more than just one line
"""
print("Hello, World!")
