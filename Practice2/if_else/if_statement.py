# if_statement.py
# Example 1: Basic if statement
a = 33
b = 200
if b > a:
    print("b is greater than a")

# Example 2: Checking positive number
number = 15
if number > 0:
    print("The number is positive")

# Example 3: Multiple statements in if
age = 20
if age >= 18:
    print("You are an adult")
    print("You can vote")
    print("You have full legal rights")

# Example 4: Using boolean variable
is_logged_in = True
if is_logged_in:
    print("Welcome back!")

# Example 5: Pass statement
a = 33
b = 200
if b > a:
    pass
