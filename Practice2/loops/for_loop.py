# for_loop.py
# Example 1: Iterating over list
fruits = ["apple", "banana", "cherry"]
for x in fruits:
    print(x)

# Example 2: Iterating over string
for x in "banana":
    print(x)

# Example 3: range() function basic
for x in range(6):
    print(x)

# Example 4: range() with start parameter
for x in range(2, 6):
    print(x)

# Example 5: range() with step parameter
for x in range(2, 30, 3):
    print(x)
